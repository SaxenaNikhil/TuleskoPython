# Working with Operators
# Arithmetic operator - +, - , *, /
x = 2
x = x + 2
print(x)

x += 2
print(x)
# same concepts for other arithmetic operator as well


# Assignment Operator
a,b = 5,9
print(a)
print(b)

# Unary Operator
n = 7
print(-n)
print(n)
# Now we will do assign the negative value to the n
n = -n
print(n)

# Logical Operator
a = 5
b = 4
print(a < 8 and b < 5)
# Same for 'or', 'not'

